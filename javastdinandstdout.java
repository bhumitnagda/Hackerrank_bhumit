//Java Stdin and Stdout I
import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        
        
        int a = scan.nextInt();
        System.out.println(a);
        
        int b = scan.nextInt();
        System.out.println(b);
        
        int c = scan.nextInt();
        System.out.println(c);
    
        scan.close();
    }
}

